<template>
  <div class="overflow-auto">
    <div class="mt-3">
      <b-pagination
        v-model="currentPage"
        :total-rows="rows"
        :per-page="perPage"
        pills
        aria-controls="my-table"
        class="m-20"
        align="center"
      ></b-pagination>
    </div>
    <b-table
      id="my-table"
      :items="items"
      :per-page="perPage"
      :current-page="currentPage"
      small
    ></b-table>
  </div>
</template>

<script>
export default {
  props: {
    items: {
      type: Array,
      require,
    },
    perPage: {
      type: Number,
      default: 9,
    },
  },
  data() {
    return {
      currentPage: 1,
    };
  },
  computed: {
    rows() {
      return this.items.length;
    },
  },
};
</script>